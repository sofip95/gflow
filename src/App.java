	public double sumar(double valorA, double valorB) {
		return valorA + valorB;
	}
	
	public double restar(double valorA, double valorB) {
		return valorA - valorB;
	}
	
	public static double multiplicar(double valorA, double valorB) {
		return valorA * valorB;
	}
	
	public static double dividir(double valorA, double valorB) {
		return valorA / valorB;
	}

	public static String cadena(String a, String b) {
		return a+b;
	}
